package br.com.tcc.controller.form;

public class RespostaQuis {
	
	private Integer numeroDaPergunta;
	private String respostaDaPergunta;
	
	
	public Integer getNumeroDaPergunta() {
		return numeroDaPergunta;
	}
	public void setNumeroDaPergunta(Integer numeroDaPergunta) {
		this.numeroDaPergunta = numeroDaPergunta;
	}
	public String getRespostaDaPergunta() {
		return respostaDaPergunta;
	}
	public void setRespostaDaPergunta(String respostaDaPergunta) {
		this.respostaDaPergunta = respostaDaPergunta;
	}
}
